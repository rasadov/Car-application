class User():
    def __init__(self, usern, name, surname, birthday, password):
        self.usern = usern
        self.surname = surname
        self.name = name
        self.birthday = birthday
        self.password = password


class Car():
    def __init__(self, producer, model, type, year, hp, cap, color, owner):
        self.producer = producer
        self.model = model
        self.type = type
        self.year = year
        self.hp = hp
        self.cap = cap
        self.color = color
        self.owner = owner
